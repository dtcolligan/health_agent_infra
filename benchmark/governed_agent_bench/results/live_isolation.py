def _probe_task(
    *,
    task_id: str,
    title: str,
    fixture_ref: str,
    off_mode: str,
    label: str,
    command_sequence: list[str],
    expected_outcome: str,
    metrics: list[str],
) -> dict[str, Any]:
    return {
        "schema_version": "governed_agent_bench.task.v2",
        "task_id": task_id,
        "title": title,
        "level": "L6",
        "runtime": "hai",
        "contract_version": "agent_cli_contract.v2",
        "allowed_context": {
            "fixture_refs": [fixture_ref],
            "manifest_ref": "hai_0_2_0",
        },
        "expected_behavior": {
            "command_sequence": [
                {"command": command, "notes": "Live isolation probe command."}
                for command in command_sequence
            ],
            "outcome": expected_outcome,
        },
        "load_bearing_mechanisms": [label],
        "metrics": metrics,
        "runtime_modes_in_scope": ["full_contract", off_mode],
        "tags": ["live_isolation", label],
        "user_prompt": (
            "Synthetic live-isolation probe task. This task is generated by "
            "results/live_isolation.py and is not part of the 28-task "
            "static oracle-pair suite."
        ),
    }


def _command(command: str, args: dict[str, Any], reason: str) -> dict[str, Any]:
    return {
        "schema_version": "governed_agent_bench.operator_action.v1",
        "action_type": "command",
        "command": command,
        "args": args,
        "reason": reason,
    }


