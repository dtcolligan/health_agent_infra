def _markers(trajectory: dict[str, Any]) -> list[str]:
    return [
        step["mechanism"]
        for step in trajectory["steps"]
        if step.get("step_type") == "mechanism_disabled"
    ]


def _contaminated(score: dict[str, Any]) -> bool:
    return any(
        v["kind"] == "mechanism_disabled_unexpected"
        for v in score.get("violations", [])
    )


def _fixture_metadata(fixture_root: Path) -> dict[str, Any]:
    path = fixture_root / "fixture_metadata.json"
    if not path.exists():
        return {}
    return json.loads(path.read_text(encoding="utf-8"))


def _changed_metrics(
    full_score: dict[str, Any],
    off_score: dict[str, Any],
) -> list[str]:
    changed: list[str] = []
    full_metrics = full_score.get("metrics", {})
    off_metrics = off_score.get("metrics", {})
    for metric in sorted(set(full_metrics) & set(off_metrics)):
        if full_metrics[metric].get("value") != off_metrics[metric].get("value"):
            changed.append(metric)
    return changed


def _metric_values(score: dict[str, Any]) -> dict[str, Any]:
    return {
        name: metric.get("value")
        for name, metric in sorted(score.get("metrics", {}).items())
    }


def _violation_kinds(score: dict[str, Any]) -> list[str]:
    return sorted({v["kind"] for v in score.get("violations", [])})


def _exit_codes(trajectory: dict[str, Any]) -> list[str]:
    return [
        str(step.get("exit_code"))
        for step in trajectory.get("steps", [])
        if step.get("step_type") == "observation"
    ]


def _run_probe(probe: dict[str, Any], workspace: Path) -> dict[str, Any]:
    task = probe["task"]
    manifest = load_manifest_snapshot(task["allowed_context"]["manifest_ref"])
    scored: dict[str, Any] = {}
    for mode in ("full_contract", probe["off_mode"]):
        fixture_root = fixture_for_task(
            task,
            fixture_workspace=workspace / "fixtures" / probe["label"] / mode,
            python_executable=sys.executable,
        )
        metadata = _fixture_metadata(fixture_root)
        actions = probe["actions"](fixture_root, metadata)
        out = workspace / "runs" / probe["label"] / mode
        out.mkdir(parents=True, exist_ok=True)
        trajectory = run_operator_actions(
            task,
            actions,
            HarnessConfig(
                fixture_root=fixture_root,
                output_dir=out,
                runtime_mode=mode,
                system_id="live_isolation_v1",
                model_class=probe["model_class"],
                invocation_context=probe["invocation_context"],
                python_executable=sys.executable,
            ),
        )
        score_manifest = manifest if probe.get("score_manifest_snapshot", True) else None
        score = score_trajectory(
            task,
            trajectory,
            manifest_snapshot=score_manifest,
            observation_root=out,
        )
        scored[mode] = {"trajectory": trajectory, "score": score}

    full = scored["full_contract"]
    off = scored[probe["off_mode"]]
    label = probe["label"]
    changed = _changed_metrics(full["score"], off["score"])
    expected_changed = set(probe["expected_changed_metrics"])
    live_isolated = (
        not _markers(full["trajectory"])
        and label in _markers(off["trajectory"])
        and not _contaminated(full["score"])
        and not _contaminated(off["score"])
        and full["score"]["overall_pass"] is True
        and off["score"]["overall_pass"] is False
        and expected_changed.issubset(changed)
    )
    return {
        "label": label,
        "off_mode": probe["off_mode"],
        "task_id": task["task_id"],
        "status": "LIVE",
        "model_class": probe["model_class"],
        "invocation_context": probe["invocation_context"],
        "full_markers": _markers(full["trajectory"]),
        "off_markers": _markers(off["trajectory"]),
        "full_exit_codes": _exit_codes(full["trajectory"]),
        "off_exit_codes": _exit_codes(off["trajectory"]),
        "full_overall_pass": full["score"]["overall_pass"],
        "off_overall_pass": off["score"]["overall_pass"],
        "changed_metrics": changed,
        "expected_changed_metrics": sorted(expected_changed),
        "full_metric_values": _metric_values(full["score"]),
        "off_metric_values": _metric_values(off["score"]),
        "full_violation_kinds": _violation_kinds(full["score"]),
        "off_violation_kinds": _violation_kinds(off["score"]),
        "live_isolated": live_isolated,
        **(
            {"scoring_note": probe["scoring_note"]}
            if probe.get("scoring_note")
            else {}
        ),
    }


def build_live_isolation_matrix(workspace: Path) -> dict[str, Any]:
    for child in ("fixtures", "runs"):
        shutil.rmtree(workspace / child, ignore_errors=True)
    workspace.mkdir(parents=True, exist_ok=True)
    rows = [_run_probe(probe, workspace) for probe in LIVE_PROBES]
    rows.sort(key=lambda row: row["label"])
    live_rows = [r for r in rows if r["status"] == "LIVE"]
    return {
        "schema_version": SCHEMA_VERSION,
        "evidence_tier": "live_runtime_probe",
        "scope_note": (
            "Targeted hermetic HAI subprocess probes now cover M4-M8 live "
            "disable paths. These are mechanism probes, not model-result "
            "trajectories; the static oracle-pair matrix remains the "
            "28-task coverage canary."
        ),
        "live_count": len(live_rows),
        "all_live_isolated": all(r["live_isolated"] for r in live_rows),
        "static_only": [],
        "live_labels": sorted(row["label"] for row in live_rows),
        "rows": rows,
    }


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--output-dir", required=True)
    args = parser.parse_args()
    out = Path(args.output_dir)
    out.mkdir(parents=True, exist_ok=True)
    matrix = build_live_isolation_matrix(out / "_work")
    path = out / "live_isolation_matrix.json"
    path.write_text(
        json.dumps(matrix, indent=2, sort_keys=True) + "\n", encoding="utf-8"
    )
    print(
        json.dumps(
            {
                "artifact": str(path),
                "live_count": matrix["live_count"],
                "live_labels": matrix["live_labels"],
                "all_live_isolated": matrix["all_live_isolated"],
                "static_only": matrix["static_only"],
            },
            indent=2,
            sort_keys=True,
        )
    )
    return 0 if matrix["all_live_isolated"] else 1


if __name__ == "__main__":
    raise SystemExit(main())
